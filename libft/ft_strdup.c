#include "libft.h"

char	*ft_strdup(char *s)
{
	size_t	i;
	char	*dst;
	size_t	len;

	i = 0;
	len = ft_strlen(s);
	dst = malloc((len + 1) * sizeof (char));
	if (!dst)
		return (NULL);
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
